// Fetch listening test data from Firebase
export async function fetchListeningTest(id: string): Promise<TestContent | null> {
  try {
    // Extract book number and test number from id format (e.g. cambridge19_ls_test1)
    const bookMatch = id.match(/cambridge(\d+)_ls_test(\d+)/);
    
    if (!bookMatch || bookMatch.length < 3) {
      console.error(`Invalid listening test id format: ${id}`);
      return null;
    }
    
    const bookNum = bookMatch[1];
    const testNum = bookMatch[2];
    const bookId = `cambridge${bookNum}`;
    const testId = `Test${testNum}`;

    console.log(`Fetching listening test: Book ${bookNum}, Test ${testNum}`);
    
    // Reference to the Firestore document
    const docRef = doc(db, "ielts_listening_tests", bookId);
    const docSnapshot = await getDoc(docRef);

    if (!docSnapshot.exists()) {
      console.error(`Cambridge book ${bookNum} not found in listening tests`);
      return null;
    }

    const data = docSnapshot.data();
    if (!data.tests || !data.tests[testId]) {
      console.error(`Test ${testNum} not found in book ${bookNum}`);
      return null;
    }

    const test = data.tests[testId];
    
    // Return data in the expected TestContent format
    return {
      passages: test.audio || "", // Audio URL for listening test
      questions: test.questions || "",
      answers: test.answers || {},
    };
  } catch (error) {
    console.error(`Error fetching listening test ${id}:`, error);
    return null;
  }
}

